#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	int		length;
	char	*dest;

	i = 0;
	length = ft_strlen((char *)src);
	dest = (char *)malloc(sizeof(char) * (length + 1));
	if (!dest)
		return (NULL);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
